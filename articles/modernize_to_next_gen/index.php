                  <h2>Modernize your way to a next generation technology capability</h2>   
                       
                  <!-- paragraph -->  

                  <p>So, if now is the time to invest in your next-generation front-office system, how should you go about it? And what are you trying to get out of it?
                  Historically, the path forward would be a bold "rip and replace" style rebuild. Today, modernization offers a different approach with many benefits. Modernization 
                  implies incremental improvement that maximizes the benefit of existing (legacy) systems rather than a big bang change. Importantly, it delivers rapid outcomes especially 
                  in terms of ability to continually innovate. It also allows a more flexible cadence where other priorities interrupt as needed. The cadence and order in which things get 
                  done is at your discretion and tailored to fit changing business imperatives.</p>

                  <h2>The keys to successful modernization</h2>
                  <p>Becoming cloud and browser enabled is probably priority number one. 
                  Cloud computing benefits are well documented but the ability to move to a no-install 
                  client deployment model by combining cloud and browser-based UIs is a game changer. 
                  As a tech vendor, being able to talk to a client on Monday and have a new version in 
                  production for them to use on Tuesday really is the gift that keeps on giving.
                  <br><br> Browser-based UIs are now synonymous with new tech and are table stakes for any tech provider that 
                  wants to be considered a good bet for the future 
                  
                  <br><br>But there is another, even more powerful (yet often unsung), benefit: web service-oriented architecture 
                  also implies data-centric architecture. This means that your system natively supports collaboration as 
                  all data is stored only once. Since most capital markets systems work across teams, this gives a 
                  assive productivity boost by collapsing workflows and eliminating the clumsy way data is shared 
                  either internally between departments or externally with clients. 
                  
                  <br><br>Once the sugar rush of new eye candy subsides - <b>it's the material gains in improving your ability to 
                  change</b>, or shortening the innovation cycle, that delivers sustained advantage. A modern backend is 
                  generic, and schema driven. Keeping the data model (schema) separate so it changes while the container 
                  doesn't change optimizes your ability to react to changing client and market needs. Then, add to this a 
                  modern application development environment that better anticipates the types of changes you're likely 
                  to make and one that provides the right tools to make it as easy as possible for the broadest set of 
                  developers.
                  
                  <br><br> <b>Automating the user journey</b> by replacing mostly manual with mostly automated is one of the long-term 
                  benefits. Every year, computers can do more and more tasks, but systems built on old architecture don't 
                  make automation easy. A modernized architecture ensures any and all business functionality is invoked 
                  with or without humans in the loop, so the level of automation is dialed up as the opportunity presents 
                  itself.  
                  
                  <br><br>The final key priority is <b>making big strides with the so-called non-functionals</b>: scalability, 
                  resilience and operability. Most existing systems have lived through massive changes in volumes of data, 
                  expectations around uptime and how they are supported. Coping with this evolution has added to costs of 
                  operation and levels of technical debt. With the benefit of hindsight, a new architecture gets to build 
                  these things in from the start. When tackled this way, previously expensive, thorny problems become 
                  much more manageable.    </p>

                  
                  <h2>Biting the bullet is not as painful as you think</h2>
                  
                  <p>It's difficult to put a number on how much of a difference these improvements make to your systems, 
                  but it goes without saying that it's considerable and ultimately, they pay for themselves many times over. 
                  I know from personal experience how hard it is to kick off large replacement projects. Modernizing with an architecture that 
                  wraps around your existing systems (leveraging what works just fine for now) not only empowers your business with a succession 
                  of quick wins, it also allows you to tackle a massive effort in chunks that won't require everything else on the tech to-do 
                  list to grind to a halt. 
                   </p>

