                  <h1>Where have vendors been historically lacking</h1>
                  <p class="line"> </p>
                  <h2>Market-Oriented not Client Problem-Oriented.</h2>
                  <p>Enterprise vendor solutions typically do not help with the client’s macro objectives to improve efficiency or innovation. In the beginning, vendors focused on department or asset class verticals (i.e., a Repo trading system) that did not have to integrate across the rest of the firm. In the late ‘90s this was the place where technology was put to good use and was the low-hanging fruit where clients looked for assistance. This follow-the-trend approach worked 20 years ago, but in today’s world tech is everywhere and the client’s needs have evolved.</p>

                  <h2>Too Much Technical Debt.</h2>
                  <p>It’s hard for tech vendors to keep their tech stack up to date, as they have also had to deal with the same constant evolution that the banks and brokers have faced. They also have additional upgrade challenges since different clients can be on different versions, and even in this age of cloud and SaaS, legacy hardware is often physically installed at the client site. Ultimately though, they are not incentivized to continue to invest in their tech because they won’t get paid more and their tech tends to become embedded with the client, so their products are naturally very sticky.</p>

                  <h2>One Size Has to Fit All.</h2>
                  <p>Whether it be by design or because the economics outlined don’t support it, enterprise software vendors have become synonymous with powerful but ill-fitting solutions that slowly fit less well over time. Changes can be requested, but sometimes need to be incorporated as a “workaround” or “hack” to a particular instance. In other cases, it requires a number of clients before the requested change achieves critical mass, so deployment can take a long time, and/or cost a lot.
                  To get around this, clients are forced to create more tech debt internally with temporary solutions that create copies of the data and erode the efficiency of workflows.</p>

                  <h2>Poor Quality of Client Support</h2>
                  <p>When a client encounters an issue with vendor technology, front-line support is normally their first point of contact. But good quality technical support that’s responsive and informative is hard to find at the best of times. Running a help desk is expensive, the roles suffer from being undervalued in tech teams, so the best representatives don’t stick around, and for many issues, the client ends up figuring out the answer for themselves. Help desks are a cost center, so when the margins aren’t there it’s a naturally easy target to cut back on.</p>
