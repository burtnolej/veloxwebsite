                      <p>
                        I was invited recently to join a panel on UI/UX in Trading. In preparation I wrote down what the differences are
                        between UX & UI from my perspective, heres what I came up with
                      </p>
                      <ul>
                        <li>UX is people centric (optimal way for a human to use the system)
                        </li>
                        <li>UI is data/ I/O centric (optimal way for the system to display / retrieve data)
                        </li>
                      </ul>
                      <p>
                        In general, it’s a pretty hard topic for the panel to discuss for the following reasons
                      </p>
                      <ul>
                        <li>Lets face it – the bars pretty low – how many people can say they were genuinely blown away by trading UX.
                        </li>
                        <li>It’s hard to distinguish between systems that hard to use because of the UX or just because the systems are crap
                        (or just well overdue a modernizarion) and good UX is not possible.
                        </li>
                        <li>What scope should we really be thinking about anyway ?– Does it make sense to worry about the UX of the EMS if you
                        have to have manually update your IOI positions if the. When we say UX in trading do we really mean workflow
                        </li>
                      </ul>
                      <p>
                        Here’s the questions that came up on the panel and how we answered.
                      </p>
                      <h2>
                        Question: Setting the scene, what are the challenges for improving the user experience in the trader’s workflow and
                        what are the drivers for change?
                      </h2>
                      <ul>
                        <li>Bad user experiences, can be managed by users becoming uber familiar. There is stern reluctance to give up
                        reliability and consistency.
                        </li>
                        <li>Modern tech (browsers) simply work differently.
                        </li>
                        <li>The users don’t usually know what is the right way to think about UX (or what good looks like) – they are not
                        engineers.
                        </li>
                        <li>Tech teams are not endowed with resources that are good at building UI’s and/or they don’t know trading.
                        </li>
                        <li>The limitations of the underlying legacy data eco-systems and backend servers.
                        </li>
                      </ul>
                      <p>
                        Drivers for change are self evident.
                      </p>
                      <ul>
                        <li>Workflow not changed much in 20 years
                        </li>
                        <li>Firms can only add capacity by adding people to operate the poor UX
                        </li>
                        <li>Manual workflow means more errors
                        </li>
                        <li>Manual workflow makes it hard to match clients desire to have less touch points for more products (horizontal vs.
                        vertical operation)
                        </li>
                      </ul>
                      <h2>
                        Question: What would the optimal user experience look like, what are the required capabilities and functionality and
                        what benefits could it deliver?
                      </h2>
                      <ul>
                        <li>User specific within finite limits (I.e highly customizable) – One size does not fit all but as the panel said this
                        flexibility needs to maintainable
                        </li>
                        <li>Should work intuitively to an extent (its ok for complex UI’s to require training)
                        </li>
                        <li>It’s easy to switch between high-level exception based mode (auto-pilot) and manual operation
                        </li>
                        <li>Makes the job easier not another barrier to battle through
                        </li>
                      </ul>
                      <h2>
                        Question: Back to reality, how should trading firms get started on a UX/UI modernisation project? How can firms get buy
                        in for technology investment and what are the critical success factors for a successful implementation?
                      </h2>
                      <ul>
                        <li>UX/UI modernization is a solution not a problem. So clearly defined ROI / problem is essential) (i.e. we have 100
                        traders and we want to double volumes and ½ staff or we have 100 errors per month and we want 10)
                        </li>
                      </ul>
                      <p>
                        Among the critical success factors are :
                      </p>
                      <ul>
                        <li>Measurable objectives agreed up front
                        </li>
                        <li>Consistent stakeholder sponsorship throughout the project
                        </li>
                      </ul>
                      <h2>
                        Question: What are the main pitfalls to avoid and how can these be resolved?
                      </h2>
                      <ul>
                        <li>The ball park budget locked, recognizing that its impossible to know full cost upfront and that forcing the project
                        to end in a hard timeframe / budget will sacrifice quality
                        </li>
                        <li>A clear migration plan to get off the existing UI. Most legacy is a new product that never fully got rolled out.
                        </li>
                        <li>UX designers that only work in drawings. Only use ones that give you the html/js/css Spending way too much
                        time/money on pixel perfect widgets that look awesome but don’t actually generate any income/benefit.
                        </li>
                        <li>Putting in place a UX strategy and level of commitment that you are comfortable sticking to as all changes going
                        forward need to conform.
                        </li>
                        <li>Make sure you know exactly what the users actually do.
                        </li>
                        <li>Don’t underestimate how powerful MS excel is. Replace or Embrace?
                        </li>
                        <li>Replacing legacy is always many times harder than you think its going to be.
                        </li>
                      </ul>
                      <h2>
                        Question: What types of technologies, tools and techniques can help here, and to what extent can firms depend on vendor
                        solutions to achieve an optimal user experience?
                      </h2>
                      <ul>
                        <li>Velox (means you don’t need to become an expert in the web stack) and/or worry about a host of other
                        non-functionals.
                        </li>
                        <li>Buy & build key, as the optimal user experience will be subjective.
                        </li>
                        <li>As an industry we are very familiar with a grid and not enough time spent on getting new concepts accepted..
                        </li>
                        <li>1 UX/UI irrespective of underlying system so consolidated flow (desktop interop
                        </li>
                      </ul>
                      <h2>
                        Question: Where does the panel see real competitive advantage in optimising the user experience, and can it outweigh
                        the financial and cultural cost of change?
                      </h2>
                      <ul>
                        <li>UX is just one element of tech modernization.
                        </li>
                        <li>There is really no choice on modernizing as tech becomes outdated and unsupported and the benefits of new tech like
                        web/cloud are so great that there is a real FOMO.
                        </li>
                        <li>When you modernize it’s a once in a decade thing so you need to do it properly. Doing it properly in 2023 means
                        putting as much energy into UX and you would resilience.
                        </li>
                      </ul>
                      <h2>
                        Question: What three pieces of advice do you have for trading organisations working on UX and UI modernisation
                        projects?
                      </h2>
                      <ul>
                        <li>Think of the UX/UI and backend holistically. The UI should be a projection of the backend and business logic should
                        not be split across the two.
                        </li>
