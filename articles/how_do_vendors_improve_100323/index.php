                  <h1>How do vendors need to improve</h1>
                  <p class="line"> </p>
                  At the end of the day, vendors are going to be asked to do more, and some but not all of these efforts are going to be matched by increased fees. In addition, they are going to be required to provide services that they simply do not have the capability to perform currently.</p>

                  <p>We foresee tiering occurring in the vendor community where some can achieve this new level and expanded relationships with clients, and some cannot. The ones that succeed will be able to effectively drive internal efficiency to offset increased costs and will be able to accelerate their internal development processes, so they out innovate the competition.</p>
                  <p>These things are hard to do which is mostly why it has not occurred naturally, but they are possible. Below we discuss some of the best techniques to employ and what will likely be hallmarks of the successful few.</p>
                <h2>Attract and Retain the Best People</h2>
                <p>No technology architecture or implementation will make up for a lack of talent. Tech vendors will need to take advantage of the generational shift of tech talent away from banks to make themselves great places for engineers to ply their trade.</p>
                <p>Historically vendors have lacked staff, especially on the development side, who have real-world experience in their clients’ business. As we continue to generally see a shift of tech talent away from Wall Street to the tech industry, vendors will also benefit from the opportunity to augment their existing skill sets with people who know what it takes to address and solve challenges within a client. For confidence to grow, clients need to see that vendor “gets it.”</p>
                <h2>Buy & Build — Practice What You Preach</h2>
                <p>Much like the banks they serve, vendors have historically been forced to build and maintain a lot of low-level infrastructure themselves. Going forward, they should look to buy lower-level components so they can focus resources and energy on things that differentiate them and ultimately their clients.</p>
                <p>This will lead to being able to “modernize-in-place”, enabling them to reuse a lot of the legacy backends by rationalizing them into discrete services that form the engine house driving the new modern client facing layer.</p>
                <h2>Fast Time to Market</h2>
                <p>Even when being more focused on differentiating parts of the stack, vendors need to realize gains in the software development process. This means investing in lowcode or no code development accelerators but also creating a very tight development lifecycle that ensures functionality gets built once and used many times. They also must ensure that incentive programs for developers are well aligned to the business goals so the natural tendency to over-engineer gets curtailed.</p>
                <p>Become Data-centric and asset-class agnostic</p>
                <p>Clients need to become data-centric but so do vendors. An ineffective or even absent data strategy will be a killer for efficiency. Data needs to be carefully curated and managed, so that any data and functionality will be available in any product and new services will be easily added.</p>
                <h2>Coherent Tech Strategy</h2>
                <p>There are many different infrastructure options that are used to deliver capital markets technology, and their various supporters will paint a very black and white picture of each of their virtues ranging from security to scalability. Which strategy you pick will not determine the outcome. What’s most important is that there is one strategy and that it is implemented consistently. The consistency will pay dividends in terms of time-to-market and operational integrity.</p>


